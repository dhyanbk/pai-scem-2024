# Define two sets A and B
A = {1, 2, 3, 4}
B = {3, 4, 5, 6}

# Union of sets A and B
union_AB = A | B  # Using the bitwise OR operator

# Intersection of sets A and B
intersection_AB = A & B  # Using the bitwise AND operator

print("Union of A and B:", union_AB)
print("Intersection of A and B:", intersection_AB)
